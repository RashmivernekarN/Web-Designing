class circle
{
   public static void main(String args[])
     {
        double r,area,pi,cir;
	  r=4.5;
	  pi=3.142;
	  area=pi*r*r;
	  cir=2*pi*r;
        System.out.println("\nr="+r);
        System.out.println("\n\narea="+area);  	
        System.out.println("\n\ncircumference="+cir);  	
        
     }
}    